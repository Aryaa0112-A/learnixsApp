<script>
    // Konfigurasi AOS untuk mengoptimalkan performa di mobile
    document.addEventListener('DOMContentLoaded', function() {
        // Deteksi perangkat mobile
        const isMobile = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent) || window.innerWidth < 768;
        
        // Konfigurasi AOS
        AOS.init({
            disable: isMobile ? true : false, // Nonaktifkan di mobile
            once: true,                       // Animasi hanya sekali
            duration: 1200,                   // Durasi animasi yang lebih pendek
            delay: 0,                         // Tidak ada delay tambahan
            mirror: false,                    // Tidak perlu mirror animasi
            offset: 60                        // Offset lebih kecil
        });
        
        // Preload semua gambar untuk menghindari jank
        const preloadImages = () => {
            const images = document.querySelectorAll('img');
            images.forEach(img => {
                const src = img.getAttribute('src');
                if (src) {
                    const newImg = new Image();
                    newImg.src = src;
                }
            });
        };
        
        // Jalankan preload
        preloadImages();
    });
</script>

